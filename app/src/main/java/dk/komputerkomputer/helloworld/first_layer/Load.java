package dk.komputerkomputer.helloworld.first_layer;

public class Load {

    private String note;

    private String file;

    public Load(String note, String file) {
        this.note = note;
        this.file = file;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }
}



